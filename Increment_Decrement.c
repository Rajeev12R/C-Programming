#include<stdio.h>
int main(){

//  Post increment  -->
    int i =1;
    printf("%d \n", i++);
    printf("%d \n",i);

//  Pre increment  -->
    int a=1;
    printf("%d \n", ++a);
    printf("%d \n", a);

//  Post decrement  -->
    int j =1;
    printf("%d \n", j--);
    printf("%d \n",j);

//  Pre increment  -->
    int b=1;
    printf("%d \n", --b);
    printf("%d \n", b);


    return 0;
}